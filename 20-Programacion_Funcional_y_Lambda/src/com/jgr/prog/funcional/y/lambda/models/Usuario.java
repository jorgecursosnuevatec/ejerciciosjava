package com.jgr.prog.funcional.y.lambda.models;

public class Usuario {
	 private String nombre;
	 private int edad;

	    public String getNombre() {
	        return nombre;
	    }

	    public void setNombre(String nombre) {
	        this.nombre = nombre;
	    }

		/**
		 * @return the edad
		 */
		public int getEdad() {
			return this.edad;
		}

		/**
		 * @param edad the edad to set
		 */
		public void setEdad(int edad) {
			this.edad = edad;
		}

	    
	    
}
